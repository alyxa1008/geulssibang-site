def grid(items):
    out = ['<div class="strokegrid">']
    for ch, n, desc, strokes in items:
        out.append(f'      <div class="scard">{svg(ch, strokes)}<b>{ch} · {n}획</b><p class="st">{desc}</p></div>')
    out.append('    </div>')
    return "\n".join(out)

