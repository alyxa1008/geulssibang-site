C = ["#d1493f", "#2f6bb0", "#7a4fb0", "#d9822b"]

def badge(x, y, n):
    return (f'<circle cx="{x}" cy="{y}" r="7.5" fill="{C[n-1]}"/>'
            f'<text x="{x}" y="{y+3.2}" text-anchor="middle" font-size="9.5" font-weight="700" fill="#fff">{n}</text>')

def svg(label, strokes):
    parts = []
    for i, (shape, bx, by) in enumerate(strokes, 1):
        col = C[i-1]
        if shape.startswith("CIRCLE"):
            _, cx, cy, r = shape.split()
            parts.append(f'<circle cx="{cx}" cy="{cy}" r="{r}" fill="none" stroke="{col}" stroke-width="7"/>')
        else:
            parts.append(f'<path d="{shape}" fill="none" stroke="{col}" stroke-width="7" stroke-linecap="round" stroke-linejoin="round"/>')
        parts.append(badge(bx, by, i))
    body = "".join(parts)
    return f'<svg viewBox="0 0 100 100" width="86" height="86" role="img" aria-label="{label} 획순">{body}</svg>'

